import 'ritual_index.dart';

class Avaliador {
  void avaliar(String nome) {
    final ritual = ritualIndex[nome];
    if (ritual == null) {
      print('‚ö†Ô∏è Ritual n√£o encontrado: $nome');
      return;
    }

    // üîç L√≥gica de avalia√ß√£o simples
    final conteudo = ritual['conteudo'] ?? '';
    final aprovado = conteudo.contains('invoca√ß√£o') || conteudo.length > 50;

    ritual['status'] = aprovado ? 'aprovado' : 'reprovado';

    print(aprovado
        ? '‚úÖ Ritual aprovado: $nome'
        : '‚ùå Ritual reprovado: $nome');
  }

  void avaliarTodos() {
    for (final nome in ritualIndex.keys) {
      avaliar(nome);
    }
  }
}
